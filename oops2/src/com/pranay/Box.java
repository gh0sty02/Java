package com.pranay;

public class Box {
    float length;
    float width;
    float height;

    public Box(float length, float width, float height) {
        this.length = length;
        this.width = width;
        this.height = height;
    }
    public Box(){
        length = -1;
        width = -1;
        height = -1;
    }
}
